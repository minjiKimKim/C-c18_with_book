/*
문제8.
for문을 이용해, 특정 월의 달력을 출력하는 프로그램 작성하기
단, 그 월의 최대일수와 그 월 1일의 요일을 입력받자.
요일은 숫자로 입력받을 것이고, 0은 일요일, 1은 월요일 순이다.
*/
#include <stdio.h>
int main(void) {
	int max, first;
	printf("최대 일 수를 입력하세요 :");
	scanf("%d", &max); //최대일수 입력
	printf("1일의 요일을 입력하세요(0-일, 1-월,...) ");
	scanf("%d", &first);// 1일의 요일 입력


}